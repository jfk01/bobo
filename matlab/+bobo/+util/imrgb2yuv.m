function [yuv] = imrgb2yuv(rgb)
%--------------------------------------------------------------------------
%
% Copyright (c) 2011 Jeffrey Byrne
% $Id: imrgb2yuv.m 82 2012-08-11 21:58:47Z jebyrne $
%
%--------------------------------------------------------------------------

A = [0.299 0.587 0.144; -0.14713 -0.28886 0.436; 0.615 -0.51499 -0.10001];
M = size(rgb,1);
N = size(rgb,2);
yuv = reshape((A*reshape(rgb,[M*N 3])')',[M N 3]);
